/// Enum for different snackbar styles
enum SnackBarStyle {
  /// Style 1: Classic filled with icon and title
  classic,

  /// Style 2: Modern with frosted glass effect
  frosted,

  /// Style 3: Minimal with left border accent
  minimal,

  /// Style 4: Gradient background
  gradient,

  /// Style 5: Floating with shadow
  floating,

  /// Style 6: Material You style
  material,

  /// Style 7: iOS style notification
  ios,

  /// Style 8: Outlined with transparent background
  outlined,

  /// Style 9: Compact single line
  compact,

  /// Style 10: Expanded with action button
  expanded,

  /// Style 11: Icon only (no text container)
  iconOnly,

  /// Style 12: Neon glow effect
  neon,

  /// Style 13: Neumorphic design
  neumorphic,

  /// Style 14: Card style with elevation
  card,

  /// Style 15: Pill shaped
  pill,

  /// Style 16: Banner style (full width)
  banner,

  /// Style 17: Grunge/Textured
  grunge,

  /// Style 18: Bubble/Chat style
  bubble,

  /// Style 19: Stripe pattern
  stripe,

  /// Style 20: Pixel art style
  pixel,

  /// Style 21: Glassmorphism
  glass,

  /// Style 22: 3D effect
  threeDimensional,

  /// Style 23: Animated dots
  dotted,

  /// Style 24: Diagonal cut
  diagonal,
}
